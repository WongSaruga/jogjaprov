                    <h2 class="post-title">Merapi, Merbabu, Menoreh Diusulkan Jadi Cagar Biosfer Dunia</h2>
                    <div class="author">by <a href="../../search/{{route('home')}}">Humas</a></div>
                    <div class="meta">
                        <div class="date"><i class="fa fa-calendar fa-fw"></i> 02 Juli 2019</div>
                        <div class="tags">
                            <i class="fa fa-tag"></i> <a href="../tag/berita.html">Berita</a> </div>
                    </div>
                    <p>
                        <p><img src="../../view_image/resize_no_frame/800/800/7277/02072019-Audiensi_LIPI_Wilis1.jpg"
                                width="600" height="338" /></p>
                        <p>&nbsp;</p>
                        <p>Yogyakarta (02/07/2019) jogjaprov.go.id &ndash; Merapi, Merbabu dan Menoreh akan didaftarkan
                            sebagai cagar biosfer dunia ke UNESCO. Pendaftaran akan dilakukan September 2019 mendatang
                            dan diharapkan akan disetujui pada sidang <em>Man and The Biosphere Programme
                                International</em> <em>Coordinating Council</em> (MAB-ICC) UNESCO tahun 2020.</p>
                        <p>Hal tersebut dibahas dalam pertemuan antara Lembaga Ilmu Pengetahuan Indonesia (LIPI) dengan
                            Gubernur DIY Sri Sultan Hamengku Buwono X, Selasa (02/07) di Ruang Gadri, Kompleks
                            Kepatihan, Yogyakarta.</p>
                        <p>Rombongan yang pimpin oleh &nbsp;Ketua Komite Nasional <em>Man and Biosphere</em> UNESCO
                            Indonesia dan Deputi Bidang Ilmu Pengetahuan Hayati LIPI, Prof. Dr. Enny Sudarmonowati
                            tersebut, membahas program cagar biosfer. Cagar Biosfer sendiri merupakan laboratorium alam
                            yang didukung oleh <em>research science</em> dan pendidikan sehingga memiliki harmonisasi
                            antara manusia dan lingkungannya.</p>
                        <p>&ldquo;Semua pihak yang ada di kawasan itu harus memberikan dukungan. Dan ini sangat baik
                            sebagai modal pengelolaan berkelanjutan dan pengelolaan multi pihak. Ini akan mencakup 2
                            provinsi dan 9 kabupaten serta melibatkan NGO, Perguruan Tinggi, Pemerintah Daerah dan yang
                            lain,&rdquo; terang Enny.</p>
                        <p>Enny menyampaikan bahwa program yang telah dirancang dan direncanakan selama 2 tahun ini,
                            akan menjadi yang pertama di Jawa Tengah dan DIY dan jadi yang ke-17 di Indonesia. Usulan
                            program ini pun harus dilengkapi dengan <em>management plan </em>untuk 10 tahun mendatang.
                            <em>Management plan</em> sendiri harus dibuat dengan melibatkan banyak pihak seperti LHK,
                            PU, Pariwisata serta berbagai dinas terkait.</p>
                        <p>Lebih lanjut Enny menjelaskan mengapa dipilih Merapi, Merbabu dan Menoreh tersebut adalah
                            karena syarat cagar biosfer salah satunya adalah adanya keunikan bio diversity, bio
                            geografi, kultur serta ekosistem.</p>
                        <p>Selain ada flora fauna yang unik, disitu juga harus ada potensi pengembangan pengembangan
                            berkelanjutan juga <em>logistic resource</em> berupa riset<em> science technology
                                education</em>. Merapi, Merbabu dan Menoreh memenuhi syarat tersebut, sehingga
                            diharapkan pengajuan ini akan disetujui oleh UNESCO.</p>
                        <p>&ldquo;Jadi ini adalah alasan kita mengangkat itu (Merapi, Merbabu, Menoreh). Ini yang sedang
                            dikemas oleh teman-teman bagaimana bisa disetujui oleh UNESCO. Karena memang tidak mudah
                            untuk mendapatkan persetujuan itu,&rdquo; jelas Enny.</p>
                        <p><img src="../../view_image/resize_no_frame/800/800/7278/02072019-Audiensi_LIPI_Wilis2.jpg"
                                width="400" height="225" /></p>
                        <p>Nantinya luas cagar biosfer ini 250.000 hadengan kawasan inti hanya sekitar 12.000 ha. Yang
                            di tetapkan sebagia &nbsp;zona inti adalah kawasan konservasi yang biasa di kelola oleh
                            KLHK. Sedangkan untuk zona <em>buffer</em> dan zona transisi akan dikelola oleh Pemda. Oleh
                            karena itu Enny meminta dukungan pada Gubernur DIY untuk membentuk badan pengelola cagar
                            biosfer.</p>
                        <p>&ldquo;Teman-teman nanti akan duduk mempersiapkan semuanya termasuk menyusun 10 tahun
                            <em>management plan</em>. Dan saya benar-benar memastikan harus semua <em>stakeholder</em>
                            ikut untuk kaitannya dengan pengelolaan berkelanjutan,&rdquo; tandas Enny.</p>
                        <p>Menanggapi rencana tersebut, Enny menyampaikan Gubernur DIY sangat antusias dan emnyambut
                            gembira. Sempat dijelaskan oleh Gubernur DIY beberapa masalah ego struktural yang ada di
                            wilayah-wilayah yang akan di usulkan menjadi cagar biosfer tersebut.</p>
                        <p>Namun demikian, apabila ini nantinya diterima oleh UNESCO, maka akan menjadi jaringan cagar
                            biosfer dunia. Gubernur DIY optimis bahwa apa yang menjadi permasalah dalam wilayah-wilayah
                            tersebut tentunya tidak hanya akan dipikirkan oleh Pemda terkait, namun juga akan dipikirkan
                            oleh seluruh dunia. Dengan demikian, segala permasalahan akan lebih mudah teratasi. (uk)</p>
                        <p>HUMAS DIY<em><br /></em></p>
