                    <br />
                    <p>
                        <div>BOS tahun 2017 Proporsi penyaluran dana ke rekening sekolah &nbsp;tiap triwulan &nbsp;dari
                            RKUN ke RKUD diatur dengan ketentuan persentase sebagai berikut&nbsp;</div>
                        <div>&nbsp;a. Triwulan I :&nbsp;<strong>20%</strong>&nbsp;<strong>dari alokasi satu
                                tahun;</strong></div>
                        <div>&nbsp;b. Triwulan II :&nbsp;<strong>40% dari alokasi satu tahun;</strong></div>
                        <div>&nbsp;c. Triwulan III :&nbsp;<strong>20% dari alokasi satu tahun;</strong></div>
                        <div>&nbsp;d. Triwulan IV :<strong>&nbsp;20% dari alokasi satu tahun.</strong></div>
                        <div>&nbsp;</div>
                        <div>Dari kondisi di atas, sekolah tidak perlu risau ataupun galau jika dana yang masuk ke
                            sekolah tidak sesuai dengan jumlah siswa karena aturanya memang
                            baru&nbsp;<strong>20%</strong>&nbsp;dana yang dicairkan dari alokasi total satu tahun.</div>
                        <div>&nbsp;</div>
                        <div>Berikut ini adalah nominal perhitungan Triwulan I, III, dan IV (proporsi 20% dari alokasi
                            satu tahun), silahkan sekolah menghitung sendiri sesuai jumlah siswanya</div>
                        <div><strong>SD</strong></div>
                        <div>Dana BOS = alokasi jumlah siswa x Rp 160.000,-<br /><br /></div>
                        <div><strong>SMP/SMP Satap&nbsp;</strong></div>
                        <div>Dana BOS = alokasi jumlah siswa x Rp 200.000,-</div>
                        <div>&nbsp;</div>
                        <div><strong>SMA/SMA Satap&nbsp;</strong></div>
                        <div>Dana BOS = alokasi jumlah siswa x Rp 280.000</div>
                        <div>&nbsp;</div>
                        <div><strong>SMK&nbsp;</strong></div>
                        <div>Dana BOS = alokasi jumlah siswa x Rp 280.000</div>
                        <div>&nbsp;</div>
                        <div><strong>SDLB yang berdiri sendiri (tidak menjadi satu dengan SMPLB/ SMALB)&nbsp;</strong>
                        </div>
                        <div>Dana BOS = alokasi jumlah siswa x Rp 160.000</div>
                        <div><strong>&nbsp;</strong></div>
                        <div><strong>SMPLB yang berdiri sendiri (tidak menjadi satu dengan SDLB/ SMALB)&nbsp;</strong>
                        </div>
                        <div>Dana BOS = alokasi jumlah siswa x Rp 200.000,-</div>
                        <div>&nbsp;</div>
                        <div><strong>SMALB yang berdiri sendiri (tidak menjadi satu dengan SDLB/ SMPLB)&nbsp;</strong>
                        </div>
                        <div>Dana BOS = alokasi jumlah siswa x Rp 280.000,-</div>
                        <div><strong>&nbsp;</strong></div>
                        <div><strong>SLB yang memiliki siswa lintas jenjang, atau sekolah luar biasa dengan satu
                                manajemen antara SDLB, dan/atau SMPLB, dan/atau SMALB&nbsp;</strong></div>
                        <div>Dana BOS = alokasi jumlah siswa x Rp 280.000</div>
                        <div>&nbsp;</div>
                        <div>Pada tahun 2017 ini, dana BOS juga diberikan kepada SLB jenjang SMP dan SMA. Tahun
                            sebelumnya untuk SLB yang mendapatkan dana BOS hanya yang berjenjang pendidikan dasar
                            (DIKDAS) saja.</div>
